#include "calc.h"
/**
 * op_add - adds two integers
 * @a: integer to be added
 * @b: integer to be added
 * Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: subtraction
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer to be mutliplied
 * @b: integer to be multiplied
 * Return: muliplication
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: division
*/
int div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder
*/
int mod(int a, int b)
{
	return (a % b);
}
